# Задати масив з 13 цілих чисел
array = [1, 3, 5, 9, 11, 0, 12, 1, 12, 21, 22, 16, 17]

# Ввести ціле число k (k > 0)
k = int(input("Введіть ціле число k: "))
if k > 0:
    # Перевірка, якщо k > 13, то k = k % 13
    k = k % 13

    # Вивести початковий масив з кольоровим форматуванням
    print("Масив 13 елементів:")
    print("\033[34m*" + str(array[0]), end=" ")  # Синій для першого елемента з зірочкою
    for num in array[1:]:
        print("\033[31m" + str(num), end=" ")  # Червоний для решти елементів
    print("\033[0m")  # Скинути кольорове форматування

    # Зсув масиву вправо на k елементів
    shifted_array = array[-k:] + array[:-k]

    # Виділити перший елемент зірочкою
    shifted_array[0] = f"*{shifted_array[0]}"

    # Вивести результат з кольоровим форматуванням
    print(f"Зсув вправо на {k} елементи:")
    for i, num in enumerate(shifted_array):
        if i == 0:
            print("\033[34m" + str(num), end=" ")  # Синій для першого елемента з зірочкою
        else:
            print("\033[31m" + str(num), end=" ")  # Червоний для решти елементів
    print("\033[0m")  # Скинути кольорове форматування
else:
    print("Число k має бути більше 0.")
